/**
 * Flight Deck Fixture Builder
 *
 * Materializes a fully populated Garura target repository at
 * `/tmp/mdb-flight-deck-fixtures` (override via FLIGHT_DECK_FIXTURE_DIR) so
 * the user-testing validators can exercise the Flight Deck with real epic
 * branches, real product artifacts, and real STM evidence.
 *
 * The script is idempotent — it deletes and re-creates the fixture directory
 * on every run so repeated invocations always produce the same state.
 *
 * Layout produced:
 *
 *   /tmp/mdb-flight-deck-fixtures/
 *     .git/                                 ← initialised, main branch, commits
 *     README.md                             ← content committed on main
 *     .garura/
 *       core/config.yaml                    ← points at fixture paths
 *       product/
 *         product.yaml, features.yaml,
 *         architecture.yaml, tech.yaml,
 *         roadmap.yaml, scenarios.yaml, plan.yaml
 *       project/issues/
 *         E1/
 *           play-prepare-epic.yaml          ← play history
 *           play-implement-epic.yaml
 *           quality-check.yaml              ← status: fail → Needs Attention
 *           validation.yaml
 *         E2/
 *           play-prepare-epic.yaml
 *           play-implement-epic.yaml
 *
 * Branches:
 *   main             — baseline, two commits (initial + product artifacts)
 *   feat/e1-auth     — 2 commits ahead of main (implementation work on E1)
 *   feat/e2-billing  — 1 commit ahead of main
 *   feat/e3-dashboard — 0 commits ahead of main
 *
 * Usage:
 *   cd core/engine && npx tsx scripts/create-flight-deck-fixtures.ts
 *
 * Then point the dev server at it:
 *   GARURA_TARGET_REPO=/tmp/mdb-flight-deck-fixtures PORT=3200 pnpm dev
 *   curl http://localhost:3200/api/flight-deck
 */

import { execFileSync } from 'node:child_process';
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

// ---------------------------------------------------------------------------
// Paths
// ---------------------------------------------------------------------------

const SCRIPT_DIR = path.dirname(fileURLToPath(import.meta.url));
const FIXTURE_DIR = process.env.FLIGHT_DECK_FIXTURE_DIR ?? '/tmp/mdb-flight-deck-fixtures';
const ENGINE_DIR = path.resolve(SCRIPT_DIR, '..');
const REPO_ROOT = path.resolve(ENGINE_DIR, '..', '..'); // core/engine → repo root
const ARTIFACT_SOURCES = [
  // name on disk              → absolute source path
  ['product.yaml', path.join(ENGINE_DIR, 'test-fixtures/.garura/product/product.yaml')],
  ['features.yaml', path.join(ENGINE_DIR, 'test-fixtures/.garura/product/features.yaml')],
  ['architecture.yaml', path.join(ENGINE_DIR, 'test-fixtures/artifacts/architecture.yaml')],
  ['tech.yaml', path.join(ENGINE_DIR, 'test-fixtures/artifacts/tech.yaml')],
  ['roadmap.yaml', path.join(ENGINE_DIR, 'test-fixtures/.garura/product/roadmap.yaml')],
  ['scenarios.yaml', path.join(ENGINE_DIR, 'test-fixtures/.garura/product/scenarios.yaml')],
  ['plan.yaml', path.join(ENGINE_DIR, 'test-fixtures/.garura/product/plan.yaml')],
] as const;

// Deterministic commit metadata — fixtures should be reproducible.
const GIT_USER_NAME = 'garura-fixtures';
const GIT_USER_EMAIL = 'garura-fixtures@example.com';

// ---------------------------------------------------------------------------
// Utilities
// ---------------------------------------------------------------------------

function log(msg: string): void {
  // eslint-disable-next-line no-console
  console.log(`[flight-deck-fixtures] ${msg}`);
}

function run(cmd: string, args: readonly string[], cwd: string): void {
  execFileSync(cmd, args, {
    cwd,
    stdio: 'pipe',
    env: {
      ...process.env,
      // Use our deterministic identity regardless of the host's git config.
      GIT_AUTHOR_NAME: GIT_USER_NAME,
      GIT_AUTHOR_EMAIL: GIT_USER_EMAIL,
      GIT_COMMITTER_NAME: GIT_USER_NAME,
      GIT_COMMITTER_EMAIL: GIT_USER_EMAIL,
    },
  });
}

function git(cwd: string, ...args: string[]): void {
  run('git', args, cwd);
}

function writeFile(relativePath: string, contents: string, cwd: string): void {
  const fullPath = path.join(cwd, relativePath);
  fs.mkdirSync(path.dirname(fullPath), { recursive: true });
  fs.writeFileSync(fullPath, contents, 'utf-8');
}

function copyFile(sourceAbs: string, destRelative: string, cwd: string): void {
  const destAbs = path.join(cwd, destRelative);
  fs.mkdirSync(path.dirname(destAbs), { recursive: true });
  fs.copyFileSync(sourceAbs, destAbs);
}

