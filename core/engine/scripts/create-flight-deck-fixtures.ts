// ---------------------------------------------------------------------------
// Build steps
// ---------------------------------------------------------------------------

function resetFixtureDir(): void {
  if (fs.existsSync(FIXTURE_DIR)) {
    log(`removing existing ${FIXTURE_DIR}`);
    fs.rmSync(FIXTURE_DIR, { recursive: true, force: true });
  }
  fs.mkdirSync(FIXTURE_DIR, { recursive: true });
}

function initGitRepo(): void {
  log('initialising git repository on branch main');
  // `-b main` ensures the initial branch is `main` regardless of host
  // init.defaultBranch config. Supported on git ≥ 2.28.
  git(FIXTURE_DIR, 'init', '-b', 'main');
  git(FIXTURE_DIR, 'config', 'user.name', GIT_USER_NAME);
  git(FIXTURE_DIR, 'config', 'user.email', GIT_USER_EMAIL);
  // Explicitly disable GPG signing so host config doesn't block automated commits.
  git(FIXTURE_DIR, 'config', 'commit.gpgsign', 'false');
}

function writeConfigAndReadme(): void {
  writeFile('.garura/core/config.yaml', CONFIG_YAML, FIXTURE_DIR);
  writeFile('README.md', README_MD, FIXTURE_DIR);
}

function writeProductArtifacts(): void {
  log('copying product artifacts into .garura/product/');
  for (const [filename, sourcePath] of ARTIFACT_SOURCES) {
    if (!fs.existsSync(sourcePath)) {
      throw new Error(`Missing source artifact: ${sourcePath}`);
    }
    copyFile(sourcePath, path.join('.garura/product', filename), FIXTURE_DIR);
  }
}

function writeStmEvidence(): void {
  log('writing STM evidence for E1 and E2');
  writeFile(
    '.garura/project/issues/E1/play-prepare-epic.yaml',
    e1PrepareEpicPlayHistory,
    FIXTURE_DIR,
  );
  writeFile(
    '.garura/project/issues/E1/play-implement-epic.yaml',
    e1ImplementEpicPlayHistory,
    FIXTURE_DIR,
  );
  writeFile('.garura/project/issues/E1/quality-check.yaml', e1QualityCheck, FIXTURE_DIR);
  writeFile('.garura/project/issues/E1/validation.yaml', e1ValidationResult, FIXTURE_DIR);
  writeFile(
    '.garura/project/issues/E2/play-prepare-epic.yaml',
    e2PrepareEpicPlayHistory,
    FIXTURE_DIR,
  );
  writeFile(
    '.garura/project/issues/E2/play-implement-epic.yaml',
    e2ImplementEpicPlayHistory,
    FIXTURE_DIR,
  );
}

function commitAll(message: string, dateIso: string): void {
  git(FIXTURE_DIR, 'add', '-A');
  // Use deterministic commit dates so the fixture is reproducible.
  execFileSync('git', ['commit', '-m', message, '--allow-empty', '--date', dateIso], {
    cwd: FIXTURE_DIR,
    stdio: 'pipe',
    env: {
      ...process.env,
      GIT_AUTHOR_NAME: GIT_USER_NAME,
      GIT_AUTHOR_EMAIL: GIT_USER_EMAIL,
      GIT_AUTHOR_DATE: dateIso,
      GIT_COMMITTER_NAME: GIT_USER_NAME,
      GIT_COMMITTER_EMAIL: GIT_USER_EMAIL,
      GIT_COMMITTER_DATE: dateIso,
    },
  });
}

function createMainCommits(): void {
  log('creating initial commits on main');
  // Initial commit: README + config only — a realistic "empty" baseline.
  writeFile('README.md', README_MD, FIXTURE_DIR);
  writeFile('.garura/core/config.yaml', CONFIG_YAML, FIXTURE_DIR);
  commitAll('chore: initial fixture commit', '2026-04-01T10:00:00Z');

  // Second commit: product artifacts (shared by all epics).
  writeProductArtifacts();
  writeStmEvidence();
  commitAll('chore: seed product artifacts and STM evidence', '2026-04-05T10:00:00Z');
}

function createEpicBranch(
  branchName: string,
  commitMessages: readonly string[],
  baseDateIso: string,
): void {
  log(`creating branch ${branchName} with ${commitMessages.length} commits`);
  // Branch from current main HEAD.
  git(FIXTURE_DIR, 'checkout', '-b', branchName, 'main');
  // Advance the date for each commit so commit ordering is deterministic.
  const base = Date.parse(baseDateIso);
  commitMessages.forEach((msg, i) => {
    const commitDate = new Date(base + i * 3600_000).toISOString();
    // Touch a branch-scoped file so each commit has non-empty content.
    const branchFile = path.join('work', branchName.replace(/\//g, '__'), `step-${i + 1}.md`);
    writeFile(
      branchFile,
      `# ${branchName} — step ${i + 1}\n\n${msg}\n\nTimestamp: ${commitDate}\n`,
      FIXTURE_DIR,
    );
    commitAll(msg, commitDate);
  });
  git(FIXTURE_DIR, 'checkout', 'main');
}

function createEpicBranches(): void {
  // E1 — 2 commits ahead, Needs Attention (via failed quality-check STM)
  createEpicBranch(
    'feat/e1-auth',
    ['feat(auth): scaffold OAuth middleware', 'feat(auth): add token refresh regression tests'],
    '2026-04-15T10:00:00Z',
  );

  // E2 — 1 commit ahead, Implementation stage on Track
  createEpicBranch(
    'feat/e2-billing',
    ['feat(billing): initial webhook handler scaffolding'],
    '2026-04-16T13:00:00Z',
  );

  // E3 — 0 commits ahead, Preparing stage (tech.yaml present but no new work)
  log('creating branch feat/e3-dashboard at main (0 commits ahead)');
  git(FIXTURE_DIR, 'checkout', '-b', 'feat/e3-dashboard', 'main');
  git(FIXTURE_DIR, 'checkout', 'main');
}

// ---------------------------------------------------------------------------
// Entry point
// ---------------------------------------------------------------------------

function main(): void {
  log(`fixture target: ${FIXTURE_DIR}`);
  log(`repo root:      ${REPO_ROOT}`);

  resetFixtureDir();
  initGitRepo();
  writeConfigAndReadme();
  createMainCommits();
  createEpicBranches();

  log('branches:');
  const branches = execFileSync('git', ['branch', '-a'], { cwd: FIXTURE_DIR, encoding: 'utf-8' });
  process.stdout.write(branches);

  log('fixture build complete. Point the engine at it with:');
  log(`  GARURA_TARGET_REPO=${FIXTURE_DIR} PORT=3200 pnpm dev`);
}

main();
